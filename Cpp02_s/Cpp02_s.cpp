#include <iostream>

using namespace std;

int main()
{
	bool a = true;
	char b = 'A';
	short c = 545;
	int d = -45390;
	long e = 765409876;
	float f = 56.98;
	double g = 453.98e3;
	long double h = 6754.987656;

	cout << "a: " << a << endl;
	cout << "b: " << b << endl;
	cout << "c: " << c << endl;
	cout << "d: " << d << endl;
	cout << "e: " << e << endl;
	cout << "f: " << f << endl;
	cout << "g: " << g << endl;
	cout << "h: " << h << endl;

	return 0;
}

